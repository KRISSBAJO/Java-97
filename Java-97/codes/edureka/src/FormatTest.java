
public class FormatTest {

	public static void main(String[] args) {
		float salary = 8500.75412f;
		
		System.out.printf("%.0f", salary);
		System.out.println();
		System.out.printf("%.1f", salary);
		System.out.println();
		System.out.printf("%.2f", salary);
		System.out.println();
		System.out.printf("%.3f", salary);
		System.out.println();		
	}

}
